// Estrutura de repetição
// while (enquanto)

let i = 0
while(i < 10) {//enquanto true, ficará rodando
  console.log(i)

  i++;
}

// while é para usar quando não sabemos o momento da parada, como exemplo abaixo
let i2 = 82714
while(i2 > 2) {//enquanto true, ficará rodando
  console.log(i2)

  i2 /= 2;
}
