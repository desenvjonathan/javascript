// Grouping operator ( )

let total = 2 + 3 * 5
console.log(total) // 17, pois ele multiplica 3x5=15 e depois soma com 2 e 15+2=17

// usando  o grouping operator, podemos mudar a ordem dos fatores da seguinte maneira
let total1 = (2 + 3) * 5
console.log(total1) // 25, pois ele soma 2 + 3 e depois multiplica por 5 e 5x5=25