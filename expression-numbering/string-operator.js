// Operador de String (String Operator)

// comparison (comparação)
console.log('a' == 'a') //true
console.log('a' == 'b') //false

// concatenation (concatenação)
// Retprna a união de duas Strings
console.log('a' + 'a') //aa

let alpha = 'alpha'
console.log(alpha + 'bet')
console.log(alpha + 'bet' + 's')

alpha += 'bet'
console.log(alpha)
