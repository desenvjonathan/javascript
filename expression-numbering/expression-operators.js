/* Expressões e Operadores

  - Expressions - qualquer linha de código que resolve algo do JavaScript
  - Operators
    * Binary / Binário
    * Unary / Unário
    * Ternary / Ternário
*/

let number = 1 // Expression

console.log(number + 1) // Operator Binary - Seria 1 + 1 da expressão
// console.log(--number) // Operator Unary - Seria 1 - 1 
console.log(++number) // Operator Unary - Seria 1 + 1
console.log(typeof number) // Operator Unary - Para idenficiar qual o tipo de dado

//ÚNICO CASO DE TERNÁRIO
console.log(true ? 'alô' : 'nada') // se alô for verdadeiro = alô
console.log(false ? 'alô' : 'nada') // se alô for falso = nada