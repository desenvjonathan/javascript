// Operadores Aritméticos

// multiplicação (*)
console.log(3 * 5) //15
console.log(2.5 * 4) //10

// divisão (/)
console.log(15 / 3) //5
console.log(10 / 4) //2.5

// soma (+)
console.log(3 + 5) //8
console.log(2.5 + 2.5) //5

// subtração (-)
console.log(5 - 3) //2
console.log(5 - 2.5) //2.5



// resto da divisão(%)
let remainder // remainder = resto 
remainder = 11 % 3 // aqui será dois
console.log(remainder)

// incremento (++)
let increment = 0
increment++ // +1
increment++ // +1
console.log(increment) /* o incremento é sempre +1, ou seja, começou 0, 
incrementou duas vezes e o resultado é 2*/
/* OBS: caso a gente queira incrementar DENTRO do console.log, precisamos escrever 
console.log(++increment), pois caso escrevamos console.log(increment++), o resultado
só será atualizado na próxima linha de código */

// decremento (--)
let decrement = 0
decrement-- // -1
decrement-- // -1
console.log(decrement) /* o decremento é sempre -1, ou seja, começou 0, 
decrementou duas vezes e o resultado é -2*/
/* OBS: caso a gente queira decrementar DENTRO do console.log, precisamos escrever 
console.log(--decrement), pois caso escrevamos console.log(decrement--), o resultado
só será atualizado na próxima linha de código */

// exponencial(**)
console.log(3 ** 3) // 27
console.log (2 ** 3) // 8

