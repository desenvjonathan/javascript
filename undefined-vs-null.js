/* 
  * Undefined 
    * Indefinido (algo que realmente não vai existir)
  
  * Null
    * Nulo (é algo que existe, mas não tem nada dentro dele)
    * É considerado um objeto que não possui nada dentro
    * Diferente de Indefinido
*/
console.log(undefined) // palavra chave reservada para o JavaScript
console.log(null) // Nulo
console.log(undefined === null) // Tanto que se formos tentar igualar, o resultado é "false"


