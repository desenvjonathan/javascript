/*
  Number
    * Números

    33 // inteiros (positivo ou negativo)
    21.5 // reais (float) (positivo ou negativo)
    NaN // Not a Number
    Infinity // Infinito
*/
console.log(33) // Inteiro
console.log(21.5) // Real
console.log(33 + 21.5) // Soma = 54.5
console.log(33 / 21.5) // Divisão = 1.5348837209302326
console.log(33 * 21.5) // Multiplicação = 709.5
console.log(25 % 100) // 25 porcento de 100 porcento = 25%
console.log(21 / "asfas") // NaN - Not a Number
console.log(21.5 === Infinity) // === significa tentar igualar e voltará o resultado de que essa expressão é falsa
console.log(Infinity) // Infinito ( o I precisa ser maiúsculo)
